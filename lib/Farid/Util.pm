sub lang2name ($self, $lang) {
    return LANGUAGES()->{$lang};
}

sub s2datetime ($self, $s) {
    my @tm = gmtime($s);
    my $time = sprintf(
        "%04d-%02d-%02d %02d:%02d:%02dZ",
        $tm[5]+1900,
        $tm[4]+1,
        $tm[3],
        $tm[2],
        $tm[1],
        $tm[0],
    );

    return $time;
}

